
public class Ejercicio3_1 {

	public static void main(String[] args) {
		//Con un bucle WHILE
		int contador = 100;
		
		//Mientras el valor sea mayor que cero
		while (contador>0) {
			//Sacamos el dato por pantalla
			System.out.println(contador);
			//Reducimos su valor en 10
			contador-=10;
		}

	}

}
